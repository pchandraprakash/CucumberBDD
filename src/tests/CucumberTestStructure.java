package tests;

public class CucumberTestStructure {
	/*
	 * Step 1: Feature(s) (define features) Login: Login should be quick and
	 * friendly
	 * 
	 * Step 2: Scenario(s) (define test scenarios) Scenario 1: Successful login User
	 * must be taken to home page after successful login and greeted with a welcome
	 * message
	 * 
	 * Step 3: Test Steps Test steps are written using Gherkin language which
	 * contains keywords like; Given, When, Then, And, But etc.
	 * 
	 * Example 1: Given: a login link/screen When: I login with valid credentials
	 * Then: I should navigate to home page And: I should see a personalized
	 * greeting message
	 * 
	 * Example 2: Scenario: Unsuccessful login User must see an invalid
	 * username/password error message after trying unsuccessful login
	 * 
	 * Given: a login link/login screen But: I enter incorrect username/password
	 * combination Then: I should see an error message pointing incorrect
	 * username/passowrd. And: I should be offered the option "forgot password?"
	 */
}
